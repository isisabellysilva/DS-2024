dia = int(input('Digite um numero de 1 a 7: '))
if dia == 1:
    print("Domingo")
if dia == 2:
    print("Segunda-Feira")
if dia == 3:
    print("Terceira-Feira")
if dia == 4:
    print("Quarta-Feira")
if dia == 5:
    print("Quinta-Feira")
if dia == 6:
    print("Sexta-Feira")
if dia == 7:
    print("Sabado")
if dia >= 7:
    print("Erro(Digite um dia abaixo de 7)")

